"""
Introduction and Explanation:
Operators in Python:
- Arithmetic operators: `+`, `-`, `*`, `/`, `%`, `**`, `//`
- Comparison operators: `==`, `!=`, `<`, `>`, `<=`, `>=`
- Logical operators: `and`, `or`, `not`
- Bitwise operators: `&`, `|`, `^`, `~`, `<<`, `>>`

Summary:
Python provides a variety of operators for arithmetic, comparison, logical, and bitwise operations, enabling manipulation and evaluation of data.
"""

# Example of Operators
# Arithmetic operators
num1 = 10
num2 = 3
print("Addition:", num1 + num2)
print("Subtraction:", num1 - num2)
print("Multiplication:", num1 * num2)
print("Division:", num1 / num2)

# Comparison operators
print("Greater than:", num1 > num2)
print("Less than or equal to:", num1 <= num2)

# Logical operators
bool1 = True
bool2 = False
print("Logical AND:", bool1 and bool2)
print("Logical OR:", bool1 or bool2)

# Bitwise operators
print("Bitwise AND:", num1 & num2)
print("Bitwise OR:", num1 | num2)
